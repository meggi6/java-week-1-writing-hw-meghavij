package javaweek1hwfeedback;
/**
 * 8. Write a Java program to divide two numbers and print on the screen.
 * Test Data: 50/3
 * Expected Output: 16
 */
public class Programme_8_DivisionOfTwoNumbers {
    public static void main(String[] args) {
        //The two integers are a and b
        int a = 50;
        int b = 3;
        int division = a / b;
        //concatenation
        System.out.println("The " + a + " is divided by " + b + " the answer is " + division);
    }
}
